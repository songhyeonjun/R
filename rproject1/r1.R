# 변수에 2개의 넣고,
## 사칙연산과
# 더한 값의 절대값(abs())과 
# 더한 결과값의 루트값(sqrt())을 
#구해서 실행
# 나머지 (%%)를 구현해보기 

# packages 탭 사용
# packages 설치 ggplot2

# 명령어 사용
# cowsay
# install.packages("cowsay")

a <- 100
b <- 200

print(a + b)
print(a - b)
print(a * b)
print(a / b)

print(abs(a + b))
print(abs(a - b))
print(abs(a * b))
print(abs(a / b))

print(sqrt(a + b))
print(sqrt(a - b))
print(sqrt(a * b))
print(sqrt(a / b))

print(a%%b)